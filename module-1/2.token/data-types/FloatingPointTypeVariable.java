class FloatingPointTypeVariable{
	public static void main(String[] args) {
		float floatPointTypeVariable;
		floatPointTypeVariable=1.2f; // here mentioning f or F is necessary
		System.out.println(floatPointTypeVariable);

		double doublePointTypeVariable;
		doublePointTypeVariable=1.2; // here mentioning d or D or keping it as it is is a choice
		System.out.println(doublePointTypeVariable);
	}

}