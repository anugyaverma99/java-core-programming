class IntegerTypeVariable{
	public static void main(String[] args) {

		byte byteTypeVariable;                        //DECLARATION
		byteTypeVariable=1;                           //INITIALIZATION
		System.out.println(byteTypeVariable);         //UTILIZATION

		short shortTypeVariable;
		shortTypeVariable=12;
		System.out.println(shortTypeVariable);

		int intTypeVariable;
		intTypeVariable=132465;
		System.out.println(intTypeVariable);

		long longTypeVariable;
		longTypeVariable=1243567845L; // here mentioning l or L is necessary
		System.out.println(longTypeVariable);
	}
}
